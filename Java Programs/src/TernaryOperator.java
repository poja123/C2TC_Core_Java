//conditional Operator-it is used to make a decision between two values
			//Syntax -   (condn)Operand?a:b; if condn pass  prints a else it will prints b
	

public class TernaryOperator 
{
	public static void main(String[]args)
	{
	int a=45;
	int b=67;
	
	System.out.println(a>b?a:b); //as a is less than b condn fails so it will print b if
	}
}
